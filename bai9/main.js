// toán tử gán (=) dùng để gán giá trị cho biến
let x = 10; // biến x được gán giá trị 10
let y = 5; // biến y được gán giá trị 5
let z = x + y; // biến z được gán giá trị là tổng của x và y
console.log(x);
console.log(y);
console.log(z);

// gán +=, -=, *=, /=, %=
console.log("Kết quả gán +=, -=, *=, /=, %=");
let m = 10;
m += 5; // m = m + 5;
console.log(m);

let n = 8;
n -= 3; // n = n - 3;
console.log(n);

let p = 6;
p *= 2; // p = p * 2;
console.log(p);

let q = 9;
q /= 3; // q = q / 3;
console.log(q);

let r = 4;
r %= 3; // r = r % 3;
console.log(r);
